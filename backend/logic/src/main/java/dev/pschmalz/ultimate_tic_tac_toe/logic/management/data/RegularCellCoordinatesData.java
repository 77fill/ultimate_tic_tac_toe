package dev.pschmalz.ultimate_tic_tac_toe.logic.management.data;

public class RegularCellCoordinatesData {
	private int metaX, metaY, x, y;

	public int getMetaX() {
		return metaX;
	}

	public void setMetaX(int metaX) {
		this.metaX = metaX;
	}

	public int getMetaY() {
		return metaY;
	}

	public void setMetaY(int metaY) {
		this.metaY = metaY;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
