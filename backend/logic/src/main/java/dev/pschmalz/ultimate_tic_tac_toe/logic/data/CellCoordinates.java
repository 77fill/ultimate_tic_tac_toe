package dev.pschmalz.ultimate_tic_tac_toe.logic.data;

public class CellCoordinates {
	private int x, y;

	public CellCoordinates(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
